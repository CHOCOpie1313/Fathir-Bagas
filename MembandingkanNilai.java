public class MembandingkanNilai {
    public static void main(String[] args) {

        int nilaiAgus = 70;
        int nilaiAsep = 70;

        if (nilaiAgus > nilaiAsep) {
            System.out.println("Nilai Agus lebih besar daripada Nilai Asep");
        } else if (nilaiAgus < nilaiAsep) {
            System.out.println("Nilai Agus lebih kecil daripada Nilai Asep");
        } else {
            System.out.println("Nilai Agus sama dengan Nilai Asep");
        }
    }
}
